    public void demo(){

        DefaultTableModel DashboardTableModel = (DefaultTableModel)DashboardTable.getModel();
        String[] Columns = {"Register Number","Name","Gender","Date of Birth","Blood Group","City","Father Name","Mother Name","Phone Number","Email"};
        DB_Connection db_Connection = new DB_Connection();
        try{
            ResultSet resultSet = db_Connection.tabledatas();
            int temp = 0;
            while(resultSet.next()){
                if(temp == 1)
                    DashboardTableModel.addRow(new Object[]{resultSet.getString(Columns[0]),resultSet.getString(Columns[1]),resultSet.getString(Columns[2]),resultSet.getString(Columns[3]),resultSet.getString(Columns[4]),resultSet.getString(Columns[5]),resultSet.getString(Columns[6]),resultSet.getString(Columns[7]),resultSet.getString(Columns[8]),resultSet.getString(Columns[9])});
                temp = 1;
            }
        }catch(Exception e){
            System.err.println("Something Wrong"+e);
        }
    }
    
    
