    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel DashboardTableModel = (DefaultTableModel)DashboardTable.getModel();
        DB_Connection dB_Connection = new DB_Connection();
        int selectedindex = DashboardTable.getSelectedRow();
        try{
            String RegNo = DashboardTableModel.getValueAt(selectedindex, 0).toString();
            String Query = "DELETE FROM `student` WHERE `student`.`Register Number` = '"+RegNo+"'";
            System.err.println(Query);
            int temp = dB_Connection.DeleteStudent(Query);
            if(temp == 1 ){
                JOptionPane.showMessageDialog(rootPane, "Delete Successfully");
                Dashboard db = new Dashboard();
                db.show();
                dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Please select Student");
            }
            
        }catch(Exception ex){
            System.err.println("Error : "+ex);
        }	
        
        
        
    }//GEN-LAST:event_deleteActionPerformed

